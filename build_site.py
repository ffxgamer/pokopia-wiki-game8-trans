def first_match(pattern: str, text: str) -> str:
    match = re.search(pattern, text, flags=re.S)
    return clean_text(match.group(1)) if match else ""


def extract_meta_description(page_html: str) -> str:
    patterns = [
        r'<meta[^>]*name="description"[^>]*content="([^"]*)"',
        r'<meta[^>]*content="([^"]*)"[^>]*name="description"',
        r"<meta[^>]*name='description'[^>]*content='([^']*)'",
        r"<meta[^>]*content='([^']*)'[^>]*name='description'",
    ]
    for pattern in patterns:
        match = re.search(pattern, page_html, flags=re.I)
        if match:
            return clean_text(match.group(1))
    return ""


def parse_page(page_id: str, page_html: str) -> PageData:
    raw_title = first_match(r"<title>(.*?)</title>", page_html)
    raw_title = raw_title.replace("｜ゲームエイト", "").strip()
    h1 = first_match(r"<h1[^>]*>(.*?)</h1>", page_html)
    description = extract_meta_description(page_html)
    description = description.replace("｜ゲームエイト", "").strip()
    description = translate_body(description)
    updated_at = first_match(r"最終更新日：\s*([0-9]{4}\.[0-9]{2}\.[0-9]{2}\s*[0-9]{2}:[0-9]{2})", page_html)

    leads = [
        clean_text(text)
        for text in re.findall(r'<p class="a-paragraph">(.*?)</p>', page_html, flags=re.S)[:6]
    ]
    leads = [text for text in leads if len(text) > 20][:3]

    headings = []
    for text in re.findall(r"<h[23][^>]*>(.*?)</h[23]>", page_html, flags=re.S):
        heading = clean_text(text)
        if (
            heading
            and heading not in headings
            and not is_noise_heading(heading)
        ):
            headings.append(heading)
        if len(headings) >= 10:
            break

    related = []
    for href, label in re.findall(
        r'href="(/pocoapokemon/\d+)".*?>(.*?)</a>', page_html, flags=re.S
    ):
        item_id = href.rsplit("/", 1)[-1]
        title = clean_text(label)
        if (
            title
            and title not in {h1, raw_title}
            and item_id != page_id
            and (item_id, title) not in related
        ):
            related.append((item_id, title))
        if len(related) >= 12:
            break

    display_title = h1 or raw_title or f"页面 {page_id}"
    zh_title = translate_title(display_title)
    body_html = strip_duplicate_intro(extract_main_content(page_html), description, leads)
    return PageData(
        page_id=page_id,
        source_url=f"{HOME_URL}/{page_id}",
        raw_title=display_title,
        zh_title=zh_title,
        description=description,
        updated_at=updated_at,
        lead=leads,
        headings=headings,
        related=related,
        body_html=body_html,
    )


