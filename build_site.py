def update_index_to_local() -> None:
    index_path = ROOT / "index.html"
    text = index_path.read_text()
    if "const toLocalHref = (href) => {" in text:
        return
    marker = "      function createCard(item) {\n"
    helper = """      const toLocalHref = (href) => {\n        const match = href.match(/\\/(\\d+)$/);\n        return match ? `pages/${match[1]}.html` : href;\n      };\n\n"""
    if marker in text:
        text = text.replace(marker, helper + marker, 1)
    updated = text.replace('href="${item.href}"', 'href="${toLocalHref(item.href)}"', 1)
    index_path.write_text(updated)


def build_sitemap(pages: Iterable[PageData]) -> None:
    sitemap = [
        {
            "id": page.page_id,
            "title": page.zh_title,
            "raw_title": page.raw_title,
            "source_url": page.source_url,
        }
        for page in pages
    ]
    (ROOT / "pages.json").write_text(json.dumps(sitemap, ensure_ascii=False, indent=2))


def build_pages() -> None:
    home_html = ensure_source()
    page_ids = parse_page_ids(home_html)
    print(f"discovered {len(page_ids)} article ids from homepage")

    if PAGES_DIR.exists():
        shutil.rmtree(PAGES_DIR)
    PAGES_DIR.mkdir(exist_ok=True)
    CACHE_DIR.mkdir(exist_ok=True)

    results: list[PageData] = []

    def worker(page_id: str) -> PageData:
        html_text = load_page_html(page_id)
        return parse_page(page_id, html_text)

    with concurrent.futures.ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
        future_map = {executor.submit(worker, page_id): page_id for page_id in page_ids}
        for future in concurrent.futures.as_completed(future_map):
            page_id = future_map[future]
            try:
                page = future.result()
            except urllib.error.URLError as exc:
                print(f"failed {page_id}: {exc}")
                continue
            except Exception as exc:  # noqa: BLE001
                print(f"failed {page_id}: {exc}")
                continue
            results.append(page)
            (PAGES_DIR / f"{page.page_id}.html").write_text(build_page_html(page))
            print(f"built {page.page_id} {page.zh_title}")

    results.sort(key=lambda item: item.page_id)
    build_sitemap(results)
    update_index_to_local()
    print(f"generated {len(results)} local pages")


if __name__ == "__main__":
    build_pages()
