def translate(text: str) -> str:
    translated = text
    for src, dst in GLOSSARY:
        translated = translated.replace(src, dst)
    translated = translated.replace("｜", " | ")
    translated = translated.replace("（", "(").replace("）", ")")
    translated = re.sub(r"\s+", " ", translated).strip(" -")
    return translated


def translate_body(text: str) -> str:
    translated = translate(text)
    for src, dst in BODY_GLOSSARY:
        translated = translated.replace(src, dst)
    for pattern, repl in BODY_REGEX_RULES:
        translated = re.sub(pattern, repl, translated)
    translated = translated.replace("「", "“").replace("」", "”")
    translated = translated.replace("、", "，")
    translated = translated.replace("　", " ")
    translated = re.sub(r"\s+", " ", translated).strip()
    translated = remove_japanese_fragments(translated, is_title=False)
    translated = normalize_chinese_output(translated, is_title=False)
    return translated


def translate_title(text: str) -> str:
    translated = translate_body(text)
    translated = normalize_chinese_output(translated, is_title=True)
    for src, dst in TITLE_GLOSSARY:
        translated = translated.replace(src, dst)
    for pattern, repl in TITLE_REGEX_RULES:
        translated = re.sub(pattern, repl, translated)
    translated = re.sub(r"\s+", " ", translated).strip(" -")
    translated = remove_japanese_fragments(translated, is_title=True)
    translated = normalize_chinese_output(translated, is_title=True)
    return translated


