def is_noise_heading(text: str) -> bool:
    if not text or text in NOISY_HEADING_EXACT:
        return True
    return any(keyword in text for keyword in NOISY_HEADING_KEYWORDS)


def heading_slug(text: str) -> str:
    slug = translate(clean_text(text))
    slug = slug.lower().replace(" ", "-")
    slug = re.sub(r"[^0-9a-z\u4e00-\u9fff\u3040-\u30ff-]+", "-", slug)
    slug = re.sub(r"-{2,}", "-", slug).strip("-")
    return slug or "section"


def strip_duplicate_intro(body_html: str, description: str, leads: list[str]) -> str:
    expected = [clean_text(description), *(clean_text(item) for item in leads)]
    expected = [item for item in expected if item]
    if not expected or not body_html:
        return body_html
    match = re.match(r'(<section class="content-block paragraph-block">.*?</section>)(.*)', body_html, flags=re.S)
    if not match:
        return body_html
    first_block, rest = match.groups()
    first_text = clean_text(first_block)
    if any(candidate and candidate in first_text for candidate in expected):
        return rest.lstrip()
    return body_html


