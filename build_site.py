@functools.lru_cache(maxsize=2048)
def localized_link_title(href: str, fallback_label: str) -> str:
    page_id = ""
    if href.startswith("/pocoapokemon/") or href.startswith("https://game8.jp/pocoapokemon/"):
        page_id = href.rsplit("/", 1)[-1]
    if page_id.isdigit():
        path = CACHE_DIR / f"{page_id}.html"
        if path.exists():
            raw = path.read_text()
            h1 = first_match(r"<h1[^>]*>(.*?)</h1>", raw)
            title = h1 or first_match(r"<title>(.*?)</title>", raw).replace("｜ゲームエイト", "").strip()
            if title:
                return translate_title(title)
    return translate_title(fallback_label)


def render_link_list(links: list[tuple[str, str]]) -> str:
    if not links:
        return ""
    unique: list[tuple[str, str]] = []
    for href, label in links:
        if not label:
            continue
        item = (href, label)
        if item not in unique:
            unique.append(item)
    return "".join(
        f'<a class="inline-link" href="{html.escape(localize_href(href))}">{html.escape(localized_link_title(href, label))}</a>'
        for href, label in unique[:8]
    )


def extract_images(fragment: str) -> list[str]:
    images = re.findall(r'data-src="([^"]+)"', fragment)
    if not images:
        images = re.findall(r'<img[^>]+src="(https?://[^"]+)"', fragment)
    return images[:4]


def render_images(fragment: str) -> str:
    images = extract_images(fragment)
    if not images:
        return ""
    return "".join(
        f'<img class="content-image" src="{html.escape(src)}" loading="lazy" alt="文章配图" />'
        for src in images
    )


def render_text_block(text: str, links: list[tuple[str, str]] | None = None) -> str:
    text = translate_body(text)
    if not text and not links:
        return ""
    links_html = render_link_list(links or [])
    body = f"<p>{html.escape(text)}</p>" if text else ""
    return f'<div class="content-copy">{body}<div class="inline-links">{links_html}</div></div>'


def render_paragraph_block(fragment: str) -> str:
    links = [(href, clean_text(label)) for href, label in re.findall(r'<a[^>]+href="([^"]+)"[^>]*>(.*?)</a>', fragment, flags=re.S)]
    text = clean_text(re.sub(r"<a[^>]*>.*?</a>", " ", fragment, flags=re.S))
    if text.startswith("★") or "評価レビュー" in text or "おすすめゲーム" in text or "人気ゲーム" in text:
        return ""
    image_html = render_images(fragment)
    copy_html = render_text_block(text, links)
    if not image_html and not copy_html:
        return ""
    return f'<section class="content-block paragraph-block">{image_html}{copy_html}</section>'


def render_heading_block(fragment: str) -> str:
    level_match = re.search(r"<h([234])", fragment)
    level = level_match.group(1) if level_match else "2"
    raw_title = clean_text(fragment)
    title = translate_title(raw_title)
    anchor = heading_slug(raw_title)
    return f'<h{level} id="{html.escape(anchor)}" class="content-head level-{level}">{html.escape(title)}</h{level}>'


