def is_short_fragment(text: str) -> bool:
    stripped = text.strip()
    if not stripped:
        return False
    if len(stripped) <= 48 and "。" not in stripped and "\n" not in stripped:
        return True
    return False


def translate_fragment(text: str) -> str:
    if not JAPANESE_RE.search(text):
        return text

    prefix_len = len(text) - len(text.lstrip())
    suffix_len = len(text) - len(text.rstrip())
    prefix = text[:prefix_len]
    suffix = text[len(text) - suffix_len :] if suffix_len else ""
    core = text[prefix_len : len(text) - suffix_len if suffix_len else len(text)]
    stripped = core.strip()
    if not stripped:
        return text

    if is_short_fragment(stripped):
        translated = build_site.translate_title(stripped)
    else:
        translated = build_site.translate_body(stripped)

    translated = translated.strip()
    if not translated:
        translated = stripped

    return f"{prefix}{translated}{suffix}"


def translate_html_text_nodes(text: str) -> str:
    protected: list[str] = []

    def protect(match: re.Match[str]) -> str:
        protected.append(match.group(0))
        return f"__BLOCK_{len(protected) - 1}__"

    text = SCRIPT_STYLE_RE.sub(protect, text)

    def repl(match: re.Match[str]) -> str:
        inner = match.group(1)
        return f">{translate_fragment(inner)}<"

    text = TEXT_NODE_RE.sub(repl, text)

    def meta_repl(match: re.Match[str]) -> str:
        content = html.unescape(match.group(2))
        if JAPANESE_RE.search(content):
            content = build_site.translate_body(content)
            content = html.escape(content, quote=True)
        return f"{match.group(1)}{content}{match.group(3)}"

    text = META_DESC_RE.sub(meta_repl, text)

    for i, block in enumerate(protected):
        text = text.replace(f"__BLOCK_{i}__", block)
    return text


def apply_cleanup(text: str, filename: str) -> str:
    for old, new in sorted(COMMON_REPLACEMENTS, key=lambda item: len(item[0]), reverse=True):
        text = text.replace(old, new)
    for old, new in sorted(FILE_REPLACEMENTS.get(filename, []), key=lambda item: len(item[0]), reverse=True):
        text = text.replace(old, new)
    return text


def translate_pages_json() -> None:
    data = json.loads(PAGES_JSON.read_text())
    changed = False
    for entry in data:
        new_title = build_site.translate_title(entry["title"])
        if new_title != entry["title"]:
            entry["title"] = new_title
            changed = True
    if changed:
        PAGES_JSON.write_text(json.dumps(data, ensure_ascii=False, indent=2))


def main() -> None:
    for path in HTML_FILES:
        original = path.read_text()
        translated = translate_html_text_nodes(original)
        translated = apply_cleanup(translated, path.name)
        if translated != original:
            path.write_text(translated)
    translate_pages_json()


if __name__ == "__main__":
    main()
